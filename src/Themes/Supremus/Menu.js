import { createDiv, createSVGElement } from '@/utils.js';

export default class Menu {

    constructor(element, toThemes, toAbout, toTicTacToe) {
        this.rootElement = element;

        [this.leftArrow, this.page, this.arkanoid, this.tetris, this.tictactoe, this.rightArrow] = GetMenu();

        if (this.tictactoe) { this.tictactoe.addEventListener('click', toTicTacToe); }
        if (this.leftArrow) { this.leftArrow.addEventListener('click', toThemes); }
        if (this.rightArrow) { this.rightArrow.addEventListener('click', toAbout); }
    }

    Show(className) {
        this.rootElement.insertAdjacentElement('beforeEnd', this.page);

        this.page.classList.add(className);

        setTimeout(() => {
            this.page.classList.remove(className);
            this.rootElement.insertAdjacentElement('afterBegin', this.leftArrow);
            this.rootElement.insertAdjacentElement('beforeEnd', this.rightArrow);
        }, 3000)
    }

    Leave(className) {
        this.page.classList.add(className);

        this.rightArrow.remove();
        this.leftArrow.remove();

        setTimeout(() => {
            this.page.classList.remove(className);
            this.page.remove();
        }, 3000)
    }
}

